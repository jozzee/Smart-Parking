package com.rmuti.en.smartparking;

public class HistoryModel {

    private String action;
    private String time;

    public HistoryModel() {
    }

    public String getAction() {
        return action;
    }

    public void setAction(String action) {
        this.action = action;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
